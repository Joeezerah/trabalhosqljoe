package bean;

public class EstoquesBean {
	
	private int IdModelo ;
	private int Total;
	private int IdTotal;
	
	public int getIdTotal() {
		return IdTotal;
	}
	public void setIdTotal(int idTotal) {
		IdTotal = idTotal;
	}
	public int getIdModelo() {
		return IdModelo;
	}
	public void setIdModelo(int idModelo) {
		IdModelo = idModelo;
	}
	public int getTotal() {
		return Total;
	}
	public void setTotal(int total) {
		Total = total;
	}

}
